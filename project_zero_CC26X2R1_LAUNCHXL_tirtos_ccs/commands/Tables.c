     uint8_t position_part = 0;
     enum Actions_tasks actionfound = NoAction;
     do {
         if (plaintext.Data[position_part] != mycommds[position_commd].Data[position_part]) {
             position_commd++;
             position_part = 0;
             if (position_commd>=7) {
                 actionfound = NoAction;
                 break;
             }
         }
         else {
             position_part++;
             if (position_part < 15 && (plaintext.Data[position_part] != mycommds[position_commd].Data[position_part])) {
                 actionfound = NoAction;
                 break;
             }
             if (position_part == 15) {
                 actionfound =(enum Actions_tasks)position_commd;
                 break;
             }
         }
     } while (position_commd< 7 || position_part < 16);

     return actionfound;

